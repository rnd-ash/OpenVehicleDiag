pub trait EcuLogisticTrait {
    fn get_diag_address(&self) -> u32;
    fn get_name(&self) -> &str;
    fn get_group_sgbd(&self) -> &str;
    fn get_bus(&self) -> BusType;
    fn get_column(&self) -> i32;
    fn get_row(&self) -> i32;
    fn get_short_name(&self) -> &str;
    fn get_sub_diag_address(&self) -> Option<u32>;
    fn get_sub_bus_list(&self) -> &[BusType];
}

pub struct EcuLogisticEntry<'a> {
    addr: u32,
    name: &'a str,
    shortname: &'a str,
    bus: BusType,
    sub_bus_list: &'a[BusType],
    sgbd: &'a str,
    column: i32,
    row: i32,
    sub_diag_addr: Option<u32>
}

impl<'a> EcuLogisticEntry<'a> {
    const fn new(addr: u32, name: &'a str, bus: BusType, sgbd: &'a str, col: i32, row: i32) -> Self {
        Self {
            addr,
            name,
            shortname: "",
            bus,
            sub_bus_list: &[],
            sgbd,
            column: col,
            row,
            sub_diag_addr: None,
        }
    }
}

impl<'a> EcuLogisticTrait for EcuLogisticEntry<'a> {
    fn get_diag_address(&self) -> u32 {
        self.addr
    }

    fn get_name(&self) -> &'a str {
        self.name
    }

    fn get_group_sgbd(&self) -> &'a str {
        self.sgbd
    }

    fn get_bus(&self) -> BusType {
        self.bus
    }

    fn get_column(&self) -> i32 {
        self.column
    }

    fn get_row(&self) -> i32 {
        self.row
    }

    fn get_short_name(&self) -> &'a str {
        self.shortname
    }

    fn get_sub_diag_address(&self) -> Option<u32> {
        self.sub_diag_addr
    }

    fn get_sub_bus_list(&self) -> &'a [BusType] {
        self.sub_bus_list
    }
}

